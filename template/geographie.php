            <p>Il existe également de nombreuses banlieues ouvrières dans le Sud, comme Palmerstown, Crumlin et Ballyfermot.</p>
            <p>Les classes moyennes de Dublin sont souvent appelées Dublin 4, en référence au code postal de l'un des quartiers les plus riches de Dublin, dans lequel se trouvent les studios de la radio nationale, Radio Telefís Éireann, ainsi que bon nombre d'écoles et de lycées réputés, une université et les ambassades. Le campus moderne de l'University College of Dublin se trouve à la limite entre Dublin 4 et Dublin 14.</p>
            <p>En fait, le terme Dublin 4 ou son abréviation D4 peut s'appliquer à n'importe quel Dublinois de classe moyenne, aussi bien du Northside que du Southside, ou plus souvent à une attitude que l'on peut trouver partout ailleurs en Irlande. De nombreux politiciens et politologues vivent à Dublin 4, et ce quartier prend traditionnellement des positions très libérales lors des référendums sur des sujets tels que l'avortement ou le divorce. Dublin 4 est également associé à un certain accent (pas vraiment spécifique à ce quartier), que certains apprécient et d'autres abhorrent.</p>
