            <p>Son climat est typiquement océanique. On peut le comparer au climat de Vancouver. Les hivers sont doux (environ 5 °C) et les étés frais (environ 15 °C). Les précipitations, d'un total de 733 mm, sont bien réparties tout au long de l'année.</p>
            <p>Les loyers réclamés aux locataires sont très élevés. En 2019, ils se situent entre 1 300 et 1 800 euros en moyenne pour un logement avec une chambre. Les prix ont augmenté en moyenne de 70 % entre 2012 et 2020, faisant de Dublin la capitale européenne la plus onéreuse pour le montant des loyers.</p>
            <p>Il existe traditionnellement une opposition entre le nord et le sud de la ville, avec la ligne de démarcation formée par la Liffey. Le Northside est plus pauvre et ouvrier, tandis que le Southside est considéré comme plus aisé, occupé par les classes moyennes et supérieures. Cette division se retrouve dans les codes postaux attribués aux quartiers, le Nord ayant des numéros impairs tandis que les numéros pairs sont attribués aux quartiers Sud. Cette division date d'il y a des siècles, sans doute à l'époque où le comte de Kildare a construit sa résidence au Sud, à l'époque peu développé, et a été rapidement suivi par ses pairs ; quand on lui demandait pourquoi il allait s'installer au Sud, il répondait « Où je vais me suit la mode ».</p>
